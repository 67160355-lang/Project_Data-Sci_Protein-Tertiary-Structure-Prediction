import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.ensemble import RandomForestClassifier, RandomForestRegressor
from sklearn.metrics import confusion_matrix, accuracy_score, f1_score, classification_report, r2_score, mean_absolute_error, mean_squared_error
from sklearn.preprocessing import StandardScaler

# 1. โหลดข้อมูล
df = pd.read_csv('protein_no_duplicates-2.csv')

# 2. เตรียม Feature (X) และ Target (y)
features = ['F9', 'F6', 'F1', 'F3']
X = df[features]

# เตรียม Target สำหรับ 2 รูปแบบ
y_reg = df['RMSD']                                # สำหรับ Regression (ค่าตัวเลข)
median_val = df['RMSD'].median()
y_clf = (df['RMSD'] >= median_val).astype(int)    # สำหรับ Classification (0=Low, 1=High)

# 3. Scaling ข้อมูล
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# 4. แบ่งข้อมูล Train/Test (ใช้ random_state เดียวกันเพื่อให้ข้อมูลชุดเดียวกันถูกทดสอบทั้ง 2 แบบ)
X_train, X_test, y_train_clf, y_test_clf = train_test_split(X_scaled, y_clf, test_size=0.2, random_state=42)
_, _, y_train_reg, y_test_reg = train_test_split(X_scaled, y_reg, test_size=0.2, random_state=42)

print("=== เริ่มการประมวลผลโมเดลรวม ===")

# ==========================================
# PART 1: Classification (จำแนกกลุ่ม สูง/ต่ำ)
# ==========================================
clf_model = RandomForestClassifier(n_estimators=100, random_state=42)
clf_model.fit(X_train, y_train_clf)
y_pred_clf = clf_model.predict(X_test)

print("\n[1] ผลการ Classification (สูง/ต่ำ):")
print(f"- Accuracy: {accuracy_score(y_test_clf, y_pred_clf):.4f}")
print(f"- F1 Score: {f1_score(y_test_clf, y_pred_clf):.4f}")

# วาดกราฟ Confusion Matrix
plt.figure(figsize=(6, 5))
sns.heatmap(confusion_matrix(y_test_clf, y_pred_clf), annot=True, fmt='d', cmap='RdPu',
            xticklabels=['Low RMSD', 'High RMSD'], yticklabels=['Low RMSD', 'High RMSD'])
plt.title('Confusion Matrix: RMSD Classification')
plt.ylabel('True Group')
plt.xlabel('Predicted Group')
plt.savefig('confusion_matrix_combined.png')

# ==========================================
# PART 2: Regression (ทำนายค่าตัวเลขจริง)
# ==========================================
reg_model = RandomForestRegressor(n_estimators=100, random_state=42)
reg_model.fit(X_train, y_train_reg)
y_pred_reg = reg_model.predict(X_test)

print("\n[2] ผลการ Regression (ค่าตัวเลข):")
print(f"- R-squared (R2): {r2_score(y_test_reg, y_pred_reg):.4f}")
print(f"- Mean Absolute Error (MAE): {mean_absolute_error(y_test_reg, y_pred_reg):.4f}")

# วาดกราฟเปรียบเทียบค่าจริง vs ค่าทำนาย
plt.figure(figsize=(6, 6))
plt.scatter(y_test_reg, y_pred_reg, alpha=0.3, color='blue')
plt.plot([y_test_reg.min(), y_test_reg.max()], [y_test_reg.min(), y_test_reg.max()], 'r--', lw=2)
plt.title('Actual vs Predicted RMSD')
plt.xlabel('Actual RMSD')
plt.ylabel('Predicted RMSD')
plt.savefig('actual_vs_predicted_regression.png')

# 5. บันทึกผลลัพธ์เปรียบเทียบลง CSV
results_df = pd.DataFrame({
    'Actual_RMSD_Value': y_test_reg,
    'Predicted_RMSD_Value': y_pred_reg,
    'Actual_Group': y_test_clf,
    'Predicted_Group': y_pred_clf
})
results_df.to_csv('combined_prediction_results.csv', index=False)
print("\nบันทึกผลการทำนายทั้งหมดลงใน 'combined_prediction_results.csv' เรียบร้อยแล้ว")

import joblib
import json
from sklearn.pipeline import Pipeline

# 1. สร้าง Pipeline ที่รวมทั้ง Scaler และ Model เข้าด้วยกัน
# การทำแบบนี้จะช่วยให้ตอน Predict ในแอป ไม่ต้องมาสั่ง scale ข้อมูลเองด้วยมือ
pipeline = Pipeline([
    ('scaler', scaler),
    ('regressor', reg_model)
])

# 2. บันทึกไฟล์ Pipeline
import os
if not os.path.exists('model_artifacts'):
    os.makedirs('model_artifacts')
    # บันทึกไฟล์แบบบีบอัดระดับ 3 (สมดุลระหว่างความเร็วและขนาดไฟล์)
joblib.dump(pipeline, 'model_artifacts/protein_rmsd_model.pkl', compress=3)

# 3. บันทึกค่า Median และ Metadata อื่นๆ ลง JSON
metadata = {
    "model_type": "Random Forest Regressor",
    "features": features,
    "median_rmsd": float(df['RMSD'].median()),
    "r2_score": float(r2_score(y_test_reg, y_pred_reg))
}

with open('model_artifacts/model_metadata.json', 'w', encoding='utf-8') as f:
    json.dump(metadata, f, indent=4)

print("Export โมเดลและ Metadata เรียบร้อยแล้ว! นำโฟลเดอร์ model_artifacts ไปใช้กับ Streamlit ได้เลย")